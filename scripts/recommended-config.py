#!/usr/bin/env python3
"""Generate the recommended Rust lint configuration and pre-commit hook.

This script is the source of truth for the lint levels documented in
docs/recommended-lint-config.md. Edit the data structures here, then
regenerate outputs.

The --fixable-lints flag points to the scraped MachineApplicable lint list
(generated by scrape-clippy-applicability.py --format cargo-toml). Those
lints are set to "allow" so they stay silent during dev, then auto-fixed
by the pre-commit hook. Recommended lint levels always take precedence.

Usage:
    # Without fixable lints (recommended deny/warn only):
    python3 scripts/recommended-config.py --format lints
    python3 scripts/recommended-config.py --format hook

    # With fixable lints (adds allow entries + restriction -W flags):
    python3 scripts/recommended-config.py --format lints --fixable-lints fixable-lints.cargo.toml
    python3 scripts/recommended-config.py --format hook  --fixable-lints fixable-lints.cargo.toml
    python3 scripts/recommended-config.py --install      --fixable-lints fixable-lints.cargo.toml
"""

